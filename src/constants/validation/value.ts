export const VALID_VALUE = {
  commentMaxLength: 1000,
  email:
    /^[A-Za-z0-9]([-_.]?[A-Za-z0-9])*@[A-Za-z0-9]([-_.]?[A-Za-z0-9])*\.[A-Za-z]{2,3}$/,
  username: {
    min: 6,
    max: 20,
    pattern:
      /(?!((?:[A-Za-z]+)|(?:[`~!@#$%^&*()-_=+[\]{}\\|;:'",<.>/?]+)|(?:[0-9]+))$)[A-Za-z\d`~!@#$%^&*()-_=+[\]{}\\|;:'",<.>/?]{6,20}$/,
  },
  password: {
    min: 6,
    max: 30,
    pattern:
      /(?!((?:[A-Za-z]+)|(?:[`~!@#$%^&*()-_=+[\]{}\\|;:'",<.>/?]+)|(?:[0-9]+))$)[A-Za-z\d`~!@#$%^&*()-_=+[\]{}\\|;:'",<.>/?]{6,30}$/,
  },
};

export const INVALID_VALUE = {
  username: /(?:[&()+={}[\]|;:",.<>/?`])|(?:[.]{2})/g,
  password: /(?:[&()+={}[\]|;:",.<>/?`])|(?:[.]{2})/g,
};
